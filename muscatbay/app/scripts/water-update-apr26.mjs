    `${SUPABASE_URL}/rest/v1/water_monthly_consumption?on_conflict=meter_id,period`;
  const r = await fetch(url, {
    method: 'POST',
    headers: {
      ...headers,
      Prefer: 'resolution=merge-duplicates,return=representation',
    },
    body: JSON.stringify(rows),
  });
